import UIKit

extension UIColor {

    class var cloudy: UIColor! {
        return UIColor(named: "cloudy")
    }

    class var rainy: UIColor! {
        return UIColor(named: "rainy")
    }

    class var sunnyForest: UIColor! {
        return UIColor(named: "sunny_forest")
    }

    class var sunnySea: UIColor! {
        return UIColor(named: "sunny_sea")
    }
}
